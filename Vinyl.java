public class Vinyl {


    String album;
    int year;

    public Vinyl(String album, int year) {

        this.album = album;
        this.year = year;
    }


    public String getAlbum() {
        return album;
    }

    public void setAlbum(String album) {
        this.album = album;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }
}
